def localize_department(department, lang=None):
    lang = lang or current_lang()
    if lang == "en":
        return department
    return DEPARTMENT_TRANSLATIONS.get(department, {}).get(lang, department)


def localize_term(group, value, lang=None):
    lang = lang or current_lang()
    if lang == "en":
        return value
    if group == "states":
        from_state = STATE_TRANSLATIONS.get(value, {}).get(lang)
        if from_state:
            return from_state
    return TERM_TRANSLATIONS.get(group, {}).get(value, {}).get(lang, value)

def localize_scheme_text(text, lang=None):
    lang = lang or current_lang()
    if not isinstance(text, str) or lang == "en":
        return text
    if text in SCHEME_TEXT_TRANSLATIONS and lang in SCHEME_TEXT_TRANSLATIONS[text]:
        return SCHEME_TEXT_TRANSLATIONS[text][lang]
    return localize_free_text(text, lang)


