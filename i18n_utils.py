def current_lang():
    lang = st.session_state.get("language", "en") or "en"
    return lang if lang in SUPPORTED_LANGS else "en"


def t(key, lang=None, **kwargs):
    lang = lang or current_lang()
    base = get_text(key, lang)
    if base == key:
        base = EXTRA_TRANSLATIONS.get(key, {}).get(lang, EXTRA_TRANSLATIONS.get(key, {}).get("en", key))
    if kwargs:
        try:
            return base.format(**kwargs)
        except Exception:
            return base
    return base


